import UIKit

extension UIColor {
    
    class var tintBlue: UIColor {
        return UIColor.init(red: 106 / 255.0, green: 193 / 255.0, blue: 205 / 255.0, alpha: 1)
    }
    
    class var secondaryGray: UIColor {
        return UIColor.init(red: 200 / 255.0, green: 208 / 255.0, blue: 215 / 255.0, alpha: 1)
    }
    
}
